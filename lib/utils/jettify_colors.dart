import 'package:basic_utils/basic_utils.dart';

class JetiffyColor {
  static primary() {
    return ColorUtils.hexToInt('#F9F9F9');
  }

  static secondary() {
    return ColorUtils.hexToInt('#0AC690');
  }

  static inputLabels() {
    return ColorUtils.hexToInt('#4B545A');
  }

  static fadedText() {
    return ColorUtils.hexToInt('#8D8C8C');
  }

  static danger() {
    return ColorUtils.hexToInt('#E3000B');
  }
}
